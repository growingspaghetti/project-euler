pub fn the_total_of_all_the_name_scores_selection_sort() -> u32 {
    // https://www.geeksforgeeks.org/selection-sort/?ref=lbp
    fn selection_sort(vec: &mut [&str]) {
        for i in 0..vec.len() {
            let mut min_i = i;
            // Unlike bubble sort, selection sort decides the order from the first and smallest.
            // eg.
            //   outer loop i=0
            //   [3 "2" 1] // given the index of mininum = 0, v=3. it's smaller at j=1
            //   [2 3 "1"] // given the index of mininum = 1, v=2. it's smaller at j=2
            //   [1 2 3] "1" determined to be the first
            //   outer loop i=1
            //   [2 "3"] // given the index of mininum = 0, v=2. it's correct at j=1, j=3
            //   [2 3] "1" determined to be the first
            //   outer loop i=2
            //   [3]
            for j in (i + 1)..vec.len() {
                if vec[j] < vec[min_i] {
                    min_i = j;
                }
            }
            vec.swap(i, min_i);
        }
    }
    let mut names = NAMES.to_vec();
    selection_sort(&mut names);
    let mut sum = 0u32;
    let a_value = 'A' as u32;
    for (i, &name) in names.iter().enumerate() {
        sum += name.chars().map(|c| (c as u32) - a_value + 1).sum::<u32>() * (i as u32 + 1);
    }
    sum
}

