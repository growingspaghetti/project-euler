pub fn the_total_of_all_the_name_scores_quick_sort() -> u32 {
    // fn decide_pivot<'a>(x: &'a str, y: &'a str, z: &'a str) -> &'a str {
    //     if (x < y && y < z) || (z < y && y <= x) {
    //         y
    //     } else if (z < x && x < y) || (y <= x && x < z) {
    //         x
    //     } else {
    //         z
    //     }
    // }

    fn pivot(vec: &Vec<&str>, i: usize, j: usize) -> usize {
        let x = vec[i];
        let y = vec[i + (j - i) / 2];
        let z = vec[j];
        if (x < y && y < z) || (z < y && y <= x) {
            i + (j - i) / 2
        } else if (z < x && x < y) || (y <= x && x < z) {
            i
        } else {
            j
        }
    }
    // https://en.wikipedia.org/wiki/Quicksort
    fn quick_sort(vec: &mut Vec<&str>, l: usize, r: usize) {
        if l < r {
            let mut i = l;
            let mut j = r;
            //let pivot = decide_pivot(vec[i], vec[i + (j - i) / 2], vec[j]);
            let pivot = vec[pivot(vec, i, j)];

            // defragmentation to the middle |left -> middle <- right|
            // [G1(=<pivot)|<-exchange->|G2(>=pivot)]
            loop {
                while vec[i] < pivot {
                    i += 1;
                }
                while pivot < vec[j] {
                    j -= 1;
                }
                // east meets west;
                // here on the elbe on april 25, 1945
                // the forces of the frst ukrainian front
                // of the red army linked up with american forces
                if i >= j {
                    break;
                }
                vec.swap(i, j);
                i += 1;
                j -= 1;
            }
            // when r == vec's west end, 0, sorting has already reached to the end
            if i != 0 {
                quick_sort(vec, l, i - 1);
            }
            // when l == vec's east end, vec.len-1, sorting has already reached to the end
            if j != vec.len() - 1 {
                quick_sort(vec, j + 1, r);
            }
        }
    }

    let mut names = NAMES.to_vec();
    quick_sort(&mut names, 0, NAMES.len() - 1);
    let mut sum = 0u32;
    let a_value = 'A' as u32;
    for (i, &name) in names.iter().enumerate() {
        sum += name.chars().map(|c| (c as u32) - a_value + 1).sum::<u32>() * (i as u32 + 1);
    }
    sum
}

