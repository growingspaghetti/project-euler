pub fn the_total_of_all_the_name_scores_bubble_sort() -> u32 {
    // https://www.geeksforgeeks.org/bubble-sort/
    fn bubble_sort(vec: &mut Vec<&str>) {
        for i in 0..vec.len() {
            let mut swap_was_required = false;
            // - i - 1 means the ruling out of the last item from re-sorting.
            // outer loop every time makes the last item in vector the greatest.
            // eg.
            //   outer loop i=0
            //   [3 2 1]
            //   [2 3 1]
            //   [2 1 3] "3" determined to be the last
            //   outer loop i=1
            //   [2 1]
            //   [1 2] "2" determined to be the last
            //   outer loop i=2
            //   [1]
            for j in 0..vec.len() - i - 1 {
                if vec[j] > vec[j + 1] {
                    vec.swap(j, j + 1);
                    swap_was_required = true;
                }
            }
            if !swap_was_required {
                break;
            }
        }
    }
    let mut names = NAMES.to_vec();
    bubble_sort(&mut names);
    let mut sum = 0u32;
    let a_value = 'A' as u32;
    for (i, &name) in names.iter().enumerate() {
        sum += name.chars().map(|c| (c as u32) - a_value + 1).sum::<u32>() * (i as u32 + 1);
        println!("{}", "a");
    }
    sum
}

