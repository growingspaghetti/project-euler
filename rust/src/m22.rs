///
/// For example, when the list is sorted into alphabetical order, COLIN, which is worth 3 + 15 + 12 + 9 + 14 = 53, is the 938th name in the list. So, COLIN would obtain a score of 938 × 53 = 49714.
///
/// What is the total of all the name scores in the file?
///
/// ```rust
/// use self::project_euler::m22::the_total_of_all_the_name_scores_std;
/// assert_eq!(the_total_of_all_the_name_scores_std(), 871198282);
/// ```
pub fn the_total_of_all_the_name_scores_std() -> u32 {
    let mut names = NAMES.to_vec();
    names.sort();

    let mut sum = 0u32;
    let a_value = 'A' as u32;
    for (i, &name) in names.iter().enumerate() {
        sum += name.chars().map(|c| (c as u32) - a_value + 1).sum::<u32>() * (i as u32 + 1);
    }
    sum
}

